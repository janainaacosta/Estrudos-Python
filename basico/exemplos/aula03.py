# fatiamento de strings

variavel = "ola mundo"

print(variavel[4:7]) # do indice 4 até o 7
print(variavel[:7]) # do começo até o 7

print(variavel[0:5]) # do começo até fim 
print(variavel[:5]) # do começo até o fim

print(variavel[0:9:4]) # comeco - fim - 3 em 3 
print(variavel[::-1]) # de trás pra frente 
